package com.Variables;

public class Local_Variable {
	
	
	private void shakthi() {
		 int i = 10;
		 i = 23;
		 System.out.println(i);

	}
	
	private void harini() {
		int i = 15;
		System.out.println(i);

	}
	
	public static void main(String[] args) {
		Local_Variable a = new Local_Variable();
		a.shakthi();
		a.harini();
		
	}
	
	
	

}
